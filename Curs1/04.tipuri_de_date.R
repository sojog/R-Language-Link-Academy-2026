## R este un limbaj de tipul loosly typed

# Tipuri de date in R

# logical (boolean)
b <- TRUE
print(b)
print(class(b))
print(typeof(b))

b <- FALSE
print(b)
print(class(b))

## numeric
b <- 10
print(b)
print(class(b))

## numeric
b <- 10.3124
print(b)
print(class(b))

## aici devine integer (tip de date)
b <- 10L
print(class(b))

# character
b <- "salutare"
print(class(b))

